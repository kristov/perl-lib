package ACME::RPE;

use strict;
use warnings;

=head1 NAME

ACME::RPE - Rocket Propulsion Elements

=head1 SYNOPSIS

use ACME::RPE qw(...);

=head1 DESCRIPTION

Rocket Propulsion Elements is a textbook describing various mathematical
techniques for the design of rocket engines.

While working through the textbook I want to codify the formulas and
techniques in the book so I can reference them later or even use the logic for
some programs. This namespace is a collection of functions and objects that
help illustrate what is happening in the book.

